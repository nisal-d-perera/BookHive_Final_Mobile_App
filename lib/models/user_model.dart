class User {
  final int id;
  final String name;
  final String image;
  final String password;
  final String mobile;
  final String email;
  final String gender;
  final String birthday;

  User({
    required this.id,
    required this.name,
    required this.image,
    required this.password,
    required this.mobile,
    required this.email,
    required this.gender,
    required this.birthday,
  });
}
